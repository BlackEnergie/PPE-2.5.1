				<a class="anchor" id="ligue4"></a>	
				<h3 class="titre">Ligue de Lorraine de volleyball</h3>
				<p class="corps">Une machine à affranchir permet un affranchissement rapide et en nombre. Cette prestation est facturée aux ligues au coût de l'affranchissement. Chaque mois, on relie la machine à affranchir à une imprimante pour obtenir une liste de codes de gestion correspondant aux ligues associés à une quantité et un type d'affranchissement. La prise en compte de ces informations permet au CROSL d'éditer des factures.</p>
